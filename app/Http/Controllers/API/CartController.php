    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Product $product)
    {
        $qty = $request->user()->cart()->find($product->id)?->pivot?->qty ?? 0;
        $newQty = $qty + 1;

        if ($newQty > $product->qty) return response()->json([], Response::HTTP_FORBIDDEN);

        $request->user()->cart()->syncWithoutDetaching([$product->id => ['qty' => $newQty]]);
        return response()->json();
    }

